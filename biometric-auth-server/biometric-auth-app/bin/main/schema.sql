-- 기존 테이블 삭제 (서버 재기동 시 초기화)
DROP TABLE IF EXISTS BIOMETRIC_NONCE;
DROP TABLE IF EXISTS BIOMETRIC_SESSION;
DROP TABLE IF EXISTS BIOMETRIC_DEVICE;

-- ============================================================
-- 디바이스 등록 테이블
-- ============================================================
CREATE TABLE BIOMETRIC_DEVICE (
    DEVICE_ID       VARCHAR2(100)  NOT NULL,
    USER_ID         VARCHAR2(100)  NOT NULL,
    PUBLIC_KEY_B64  VARCHAR2(512),  -- KEY_INVALIDATED 시 NULL
    STATUS          VARCHAR2(20)   NOT NULL,
    ENROLLED_AT     TIMESTAMP      NOT NULL,
    UPDATED_AT      TIMESTAMP      NOT NULL,
    CONSTRAINT PK_BIOMETRIC_DEVICE PRIMARY KEY (DEVICE_ID)
);

COMMENT ON TABLE  BIOMETRIC_DEVICE                IS '생체인증 디바이스 등록 정보';
COMMENT ON COLUMN BIOMETRIC_DEVICE.DEVICE_ID      IS '디바이스 고유 ID (Android ID)';
COMMENT ON COLUMN BIOMETRIC_DEVICE.USER_ID        IS 'MIS 사용자 ID';
COMMENT ON COLUMN BIOMETRIC_DEVICE.PUBLIC_KEY_B64 IS 'ECDSA 공개키 (X.509, Base64). KEY_INVALIDATED 시 NULL';
COMMENT ON COLUMN BIOMETRIC_DEVICE.STATUS         IS '디바이스 상태 (ACTIVE/LOCKED/KEY_INVALIDATED)';
COMMENT ON COLUMN BIOMETRIC_DEVICE.ENROLLED_AT    IS '생체인식 최초 등록 일시';
COMMENT ON COLUMN BIOMETRIC_DEVICE.UPDATED_AT     IS '상태 마지막 변경 일시';

CREATE INDEX IDX_BIOMETRIC_DEVICE_USER
    ON BIOMETRIC_DEVICE (USER_ID);

-- ============================================================
-- 인증 세션 테이블
-- ============================================================
CREATE TABLE BIOMETRIC_SESSION (
    SESSION_ID      VARCHAR2(64)   NOT NULL,
    DEVICE_ID       VARCHAR2(100)  NOT NULL,
    USER_ID         VARCHAR2(100)  NOT NULL,
    CHALLENGE_HEX   VARCHAR2(64)   NOT NULL,
    CLIENT_NONCE    VARCHAR2(64)   NOT NULL,
    REQ_TIMESTAMP   NUMBER(13)     NOT NULL,
    EXPIRE_AT       TIMESTAMP      NOT NULL,
    USED_YN         CHAR(1)        NOT NULL DEFAULT 'N',
    CREATED_AT      TIMESTAMP      NOT NULL,
    CONSTRAINT PK_BIOMETRIC_SESSION PRIMARY KEY (SESSION_ID),
    CONSTRAINT FK_BIOMETRIC_SESSION_DEVICE
        FOREIGN KEY (DEVICE_ID) REFERENCES BIOMETRIC_DEVICE (DEVICE_ID)
);

COMMENT ON TABLE  BIOMETRIC_SESSION               IS '생체인증 챌린지 세션 정보';
COMMENT ON COLUMN BIOMETRIC_SESSION.SESSION_ID    IS '세션 고유 ID (UUID hex)';
COMMENT ON COLUMN BIOMETRIC_SESSION.DEVICE_ID     IS '디바이스 고유 ID (BIOMETRIC_DEVICE 참조)';
COMMENT ON COLUMN BIOMETRIC_SESSION.USER_ID       IS 'MIS 사용자 ID';
COMMENT ON COLUMN BIOMETRIC_SESSION.CHALLENGE_HEX IS '서버 생성 챌린지 값 (32바이트 random hex)';
COMMENT ON COLUMN BIOMETRIC_SESSION.CLIENT_NONCE  IS '클라이언트 생성 nonce (재전송 방지)';
COMMENT ON COLUMN BIOMETRIC_SESSION.REQ_TIMESTAMP IS '클라이언트 요청 시각 (unix milliseconds)';
COMMENT ON COLUMN BIOMETRIC_SESSION.EXPIRE_AT     IS '세션 만료 일시 (생성시각 + 60초)';
COMMENT ON COLUMN BIOMETRIC_SESSION.USED_YN       IS '세션 사용 여부 (N: 미사용 / Y: 사용완료)';
COMMENT ON COLUMN BIOMETRIC_SESSION.CREATED_AT    IS '세션 생성 일시';

CREATE INDEX IDX_BIOMETRIC_SESSION_EXPIRE
    ON BIOMETRIC_SESSION (EXPIRE_AT);

-- ============================================================
-- Nonce 중복 방지 테이블
-- ============================================================
CREATE TABLE BIOMETRIC_NONCE (
    NONCE       VARCHAR2(64)   NOT NULL,
    DEVICE_ID   VARCHAR2(100)  NOT NULL,
    USED_AT     TIMESTAMP      NOT NULL,
    EXPIRE_AT   TIMESTAMP      NOT NULL,
    CONSTRAINT PK_BIOMETRIC_NONCE PRIMARY KEY (NONCE)
);

COMMENT ON TABLE  BIOMETRIC_NONCE            IS 'Nonce 중복 사용 방지 테이블 (재전송 공격 차단)';
COMMENT ON COLUMN BIOMETRIC_NONCE.NONCE      IS '클라이언트 nonce 값 (hex32)';
COMMENT ON COLUMN BIOMETRIC_NONCE.DEVICE_ID  IS 'nonce를 사용한 디바이스 ID (감사 목적)';
COMMENT ON COLUMN BIOMETRIC_NONCE.USED_AT    IS 'nonce 최초 사용 일시';
COMMENT ON COLUMN BIOMETRIC_NONCE.EXPIRE_AT  IS 'nonce 만료 일시 (USED_AT + 5분, 이후 삭제 대상)';

CREATE INDEX IDX_BIOMETRIC_NONCE_EXPIRE
    ON BIOMETRIC_NONCE (EXPIRE_AT);
